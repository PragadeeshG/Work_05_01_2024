package com.test1;

public class SellingProposition {
	private String aspiringContent;
	private String uniqueProposition;
	private String productUniqueness;
	private String serviceUniqueness;
	private String uspStage;
	private String businessStage;
	private String productStand;
	private String serviceStand;
	private String productHighlights;
	private String serviceHighlights;
	private String strategyCode;
	private String competitors;
	private String serviceCompetitors;

	public SellingProposition() {

	}

	public SellingProposition(String aspiringContent, String uniqueProposition, String productUniqueness,
			String serviceUniqueness, String uspStage, String businessStage, String productStand, String serviceStand,
			String productHighlights, String serviceHighlights, String strategyCode, String competitors,
			String serviceCompetitors) {
		super();
		this.aspiringContent = aspiringContent;
		this.uniqueProposition = uniqueProposition;
		this.productUniqueness = productUniqueness;
		this.serviceUniqueness = serviceUniqueness;
		this.uspStage = uspStage;
		this.businessStage = businessStage;
		this.productStand = productStand;
		this.serviceStand = serviceStand;
		this.productHighlights = productHighlights;
		this.serviceHighlights = serviceHighlights;
		this.strategyCode = strategyCode;
		this.competitors = competitors;
		this.serviceCompetitors = serviceCompetitors;
	}

	public String getAspiringContent() {
		return aspiringContent;
	}

	public void setAspiringContent(String aspiringContent) {
		this.aspiringContent = aspiringContent;
	}

	public String getUniqueProposition() {
		return uniqueProposition;
	}

	public void setUniqueProposition(String uniqueProposition) {
		this.uniqueProposition = uniqueProposition;
	}

	public String getProductUniqueness() {
		return productUniqueness;
	}

	public void setProductUniqueness(String productUniqueness) {
		this.productUniqueness = productUniqueness;
	}

	public String getServiceUniqueness() {
		return serviceUniqueness;
	}

	public void setServiceUniqueness(String serviceUniqueness) {
		this.serviceUniqueness = serviceUniqueness;
	}

	public String getUspStage() {
		return uspStage;
	}

	public void setUspStage(String uspStage) {
		this.uspStage = uspStage;
	}

	public String getBusinessStage() {
		return businessStage;
	}

	public void setBusinessStage(String businessStage) {
		this.businessStage = businessStage;
	}

	public String getProductStand() {
		return productStand;
	}

	public void setProductStand(String productStand) {
		this.productStand = productStand;
	}

	public String getServiceStand() {
		return serviceStand;
	}

	public void setServiceStand(String serviceStand) {
		this.serviceStand = serviceStand;
	}

	public String getProductHighlights() {
		return productHighlights;
	}

	public void setProductHighlights(String productHighlights) {
		this.productHighlights = productHighlights;
	}

	public String getServiceHighlights() {
		return serviceHighlights;
	}

	public void setServiceHighlights(String serviceHighlights) {
		this.serviceHighlights = serviceHighlights;
	}

	public String getStrategyCode() {
		return strategyCode;
	}

	public void setStrategyCode(String strategyCode) {
		this.strategyCode = strategyCode;
	}

	public String getCompetitors() {
		return competitors;
	}

	public void setCompetitors(String competitors) {
		this.competitors = competitors;
	}

	public String getServiceCompetitors() {
		return serviceCompetitors;
	}

	public void setServiceCompetitors(String serviceCompetitors) {
		this.serviceCompetitors = serviceCompetitors;
	}

}
